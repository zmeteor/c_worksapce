//同构数

#include<stdio.h>

int main()
{
    int i ;
    printf("2～999的同构数为：");

    for(i = 2;i < 999;i++)
    {        
        if(i == i * i % 10 || i == i * i %100 ||i == i * i % 1000)
             printf("%d\t",i);
    }   

    return 0;
}

